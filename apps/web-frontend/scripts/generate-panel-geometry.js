#!/usr/bin/env node

const fs = require('fs')
const path = require('path')

const appDir = path.resolve(__dirname, '..')
const svgPath = path.join(appDir, 'src/assets/images/panel_trace_simplified.svg')
const outputPath = path.join(appDir, 'src/app/shell/panelGeometry.generated.js')

const svgSource = fs.readFileSync(svgPath, 'utf8')

function readViewBox (source) {
  const match = source.match(/viewBox="([^"]+)"/)
  if (!match) {
    throw new Error('Unable to find viewBox in panel_trace_simplified.svg')
  }

  const [x, y, width, height] = match[1].trim().split(/\s+/).map(Number)
  return { x, y, width, height }
}

function readCircleById (source, id) {
  const escapedId = id.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  const match = source.match(new RegExp(`<circle\\s+[^>]*id="${escapedId}"[^>]*cx="([^"]+)"[^>]*cy="([^"]+)"[^>]*r="([^"]+)"[^>]*/?>`))

  if (!match) {
    throw new Error(`Unable to find circle with id "${id}" in panel_trace_simplified.svg`)
  }

  return {
    cx: Number(match[1]),
    cy: Number(match[2]),
    r: Number(match[3])
  }
}

const viewBox = readViewBox(svgSource)
const anchors = {
  heroOrb: readCircleById(svgSource, 'hero-orb'),
  actionSlot1: readCircleById(svgSource, 'action-slot-1'),
  actionSlot2: readCircleById(svgSource, 'action-slot-2'),
  actionSlot3: readCircleById(svgSource, 'action-slot-3'),
  leftFooterRaMinus: readCircleById(svgSource, 'left-footer-ra-minus'),
  leftFooterDecMinus: readCircleById(svgSource, 'left-footer-dec-minus'),
  leftFooterFunc: readCircleById(svgSource, 'left-footer-func')
}

const output = `// Auto-generated by scripts/generate-panel-geometry.js
// Do not edit manually. Update src/assets/images/panel_trace_simplified.svg instead.

export const PANEL_VIEWBOX = ${JSON.stringify(viewBox, null, 2)}

export const PANEL_ANCHORS = ${JSON.stringify(anchors, null, 2)}
`

fs.writeFileSync(outputPath, output)
console.log(`Generated ${path.relative(appDir, outputPath)}`)
